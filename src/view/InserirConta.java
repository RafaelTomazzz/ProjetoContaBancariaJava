    private void InserirConta(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InserirConta
        int numero = Integer.parseInt(InputNumero.getText());
        String titular = InputTitular.getText();
        double saldo = Double.parseDouble(InputSaldo.getText());
        
        ContaCorrente conta = new ContaCorrente(numero, titular, saldo);
        boolean res = contaDAO.InserirConta(conta);
        
        if(res){
            JOptionPane.showMessageDialog(null, "Conta criada com sucesso!");
        } else{
            JOptionPane.showMessageDialog(null, "Erro!");
        }
        
        TelaPrincipal telaPrincipal = new TelaPrincipal();
        telaPrincipal.setVisible(true);
        this.dispose();
        
    }//GEN-LAST:event_InserirConta

